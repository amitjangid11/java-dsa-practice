package module_09_conditional_statement;

public class J_03_RelationalOperator {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        /*
        1. ==
        2. !=
        3. >
        4. <
        5. >=
        6. <=
         */
        // Equality operator
        System.out.println(a == b);

        // Not Equal Operator
        System.out.println(a != b);

        // Greater than
        System.out.println(a > b);

        // less than
        System.out.println(a < b);

        // Greater than equal to
        System.out.println(a >= b);

        // Less than equal to
        System.out.println(a <= b);

    }
}
