package module_08_basics;

public class VariableNamingRules_08 {
    public static void main(String[] args) {
        /*
        1) Variable can be start from any alphabet, underscore and $ not other than these three.
        2) Special character other than _ and $ are not allowed neither in starting nor in between like ! @ % ^ & * ( ) ,
        3) Some particular keywords are not allowed. like while, enum, do-while, int, etc.
        4)
         */
        int money;
        int $age;
        int _year;
        int auto; // not respected
        int _2004; // Allowed
        int FLOAT;
        int INT;


    }
}
