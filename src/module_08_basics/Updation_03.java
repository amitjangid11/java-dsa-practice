package module_08_basics;

public class Updation_03 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        a = 20; // Updation_03
        System.out.println(a);
        a = a - 10; // a -= 5; -> updation Shortcut
        System.out.println(a);
        a = a + 10; // a += 5;
        System.out.println(a);
        a = a * 5; // a *= 5;
        System.out.println(a);
        a = a / 5; // a /= 5;
        System.out.println(a);

    }
}
